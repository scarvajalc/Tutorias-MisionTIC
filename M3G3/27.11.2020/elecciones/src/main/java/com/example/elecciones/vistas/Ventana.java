    private void botonVotarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonVotarActionPerformed
       
        int cedulaVotante = Integer.parseInt(campoCedulaVotanteVotacion.getText());
        Optional<Votante> presuntoVotante = repositorioVotante.findById(cedulaVotante);
        if (presuntoVotante.isPresent()) {
            Votante votante = presuntoVotante.get();
            Candidato candidatoVotado = (Candidato)listaCandidatos.getSelectedItem();
            votante.setCandidato(candidatoVotado);
            repositorioVotante.save(votante);
            mensajes.setText("Voto registrado");
        } else {
            mensajes.setText("El votante no est√° registrado");
        }
    }//GEN-LAST:event_botonVotarActionPerformed

